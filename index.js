function activeSidebar () {
    document.getElementById('sidebarLeftMobile').classList.toggle('active');
}

function closeSidebar () {
    document.getElementById('sidebarLeftMobile').classList.toggle('close');
}

function activeMessages () {
    document.getElementById('onMessageBox').classList.toggle('on');
}


function offMessageBox () {
    document.getElementById('onMessageBox').classList.toggle('off');
}